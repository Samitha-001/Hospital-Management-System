        private void table_update(){
            int c;
            try {
//            Class.forName("com.mysql.cj.jdbc.Driver");
//            con1 = DriverManager.getConnection("jdbc:mysql://localhost/hospitaldb","root","");
            insert = con1.prepareStatement("select * from vendor_supply_drug");
            ResultSet rs = insert.executeQuery();
            ResultSetMetaData Rss = rs.getMetaData();
            c = Rss.getColumnCount();
            
            DefaultTableModel Df = (DefaultTableModel) jTable1.getModel();
            Df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for(int a=1;a<=c;a++){
                    v2.add(rs.getString("Supply_ID"));
                    v2.add(rs.getString("VendorID"));
                    v2.add(rs.getString("DrugCode"));
                    v2.add(rs.getString("Supplied_Date"));
                    v2.add(rs.getString("Quantity"));
                    
                }
                
                Df.addRow(v2);
            }
     
            
        }  catch (Exception ex) {
           
        }
            
        }
