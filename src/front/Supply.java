    private void submit_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_btnActionPerformed
       String supplyID = supid.getText();
       String vendorID = venid.getText();
       String DrugCode = drugid.getText();
       SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
       String date = sdf.format(sdate.getDate());
       String quantity = quan.getText();
       
       
        try {
//            Class.forName("com.mysql.cj.jdbc.Driver");
//            con1 = DriverManager.getConnection("jdbc:mysql://localhost/hospital","root","");
            insert = con1.prepareStatement("INSERT INTO `vendor_supply_drug`(`Supply_ID`, `VendorID`, `DrugCode`, `Supplied_Date`, `Quantity`) VALUES (?,?,?,?,?)");
            insert.setString(1, supplyID);
            insert.setString(2, vendorID);
            insert.setString(3, DrugCode);
            insert.setString(4, date);
            insert.setString(5, quantity);
            insert.executeUpdate();
            
            JOptionPane.showMessageDialog(this,"record added");
            table_update();
            
            supid.setText("");
            venid.setText("");
            drugid.setText("");
            quan.setText("");
            supid.requestFocus();
             
            
        }catch(Exception e){
        } 
       
    }//GEN-LAST:event_submit_btnActionPerformed

